package com.practice.loops;

public class PrintOne_Five_SingleLine {
	
//Here i created the main Class named PrintOne_Five_SingleLine 
	
	public static void main(String[]args)//inside the class called main method 
	{
		
		for (int i=1; i<=5;i++) //for loop starts from 1 and run upto 5
		{
			System.out.print( i );
			
			//print current value of i in each iteration 
		}
		
	}

}
