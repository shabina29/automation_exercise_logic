package com.javacode.practice;

public class LoopExample {

	/*
	 * ✅ 1. Basic For Loop (Print 1 to 5) for (int i = 1; i <= 5; i++) { // int i =
	 * 1 → loop start hota hai 1 se // i <= 5 → jab tak i 5 se chhota ya equal hai
	 * tab tak loop chalega // i++ → har iteration me i ki value 1 se increase hogi
	 * 
	 * System.out.println(i); // Har baar current value of i print hogi (1, 2, 3, 4,
	 * 5) } 🎤 Interview me kaise bolna hai
	 * 
	 * “This loop starts from 1 and runs until 5.” “In each iteration, the value of
	 * i is printed and incremented by 1.”
	 * 
	 * ==========================================================================
	 * ✅ 2. Print Array Using For Loop int[] arr = {10, 20, 30};
	 * 
	 * for (int i = 0; i < arr.length; i++) { // i = 0 → start from first index // i
	 * < arr.length → last index tak loop chalega // i++ → next index pe move karega
	 * 
	 * System.out.println(arr[i]); // arr[i] → array ka element at index i print
	 * hoga } 🎤 Interview Line
	 * 
	 * “I am iterating through the array using index and printing each element using
	 * arr[i].”
	 * 
	 * =============================================================================
	 * ✅ 3. Reverse Loop (Important) for (int i = 5; i >= 1; i--) { // i = 5 → loop
	 * start hota hai last se // i >= 1 → jab tak 1 tak nahi pahunchte // i-- → har
	 * baar 1 se decrease hota hai
	 * 
	 * System.out.println(i); // Output: 5 4 3 2 1 } 🎤 Interview Line
	 * 
	 * “Here I am running the loop in reverse order by decrementing the value.”
	 * 
	 * 🔥 Important Concept (Must Say in Interview)
	 * 
	 * 👉 “For loop is used when we know the number of iterations in advance.”
	 */

}
